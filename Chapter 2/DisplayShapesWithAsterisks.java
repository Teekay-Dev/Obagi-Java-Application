public class DisplayShapesWithAsterisks {
    public static void main(String[] args) {
        // Display a box.
        System.out.println("**********");
        System.out.println("*        *");
        System.out.println("*        *");
        System.out.println("*        *");
        System.out.println("**********");
        System.out.println(); // Blank line for separation

        // Display an oval.
        System.out.println("   ***   ");
        System.out.println(" *     * ");
        System.out.println("*       *");
        System.out.println("*       *");
        System.out.println("*       *");
        System.out.println(" *     * ");
        System.out.println("   ***   ");
        System.out.println(); // Blank line for separation

        // Display an arrow.
        System.out.println("    *    ");
        System.out.println("   ***   ");
        System.out.println("  *****  ");
        System.out.println("    *    ");
        System.out.println("    *    ");
        System.out.println("    *    ");
        System.out.println("    *    ");
        System.out.println(); // Blank line for separation

        // Display a diamond.
        System.out.println("    *    ");
        System.out.println("   ***   ");
        System.out.println("  *****  ");
        System.out.println(" ******* ");
        System.out.println("  *****  ");
        System.out.println("   ***   ");
        System.out.println("    *    ");
    }
}
